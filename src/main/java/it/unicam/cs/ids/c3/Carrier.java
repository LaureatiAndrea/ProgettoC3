package it.unicam.cs.ids.c3;

/**
 * Rappresenta un corriere addetto al trasporto delle merci.
 */
public interface Carrier {

    /**
     * Restituisce il nome del corriere.
     *
     * @return il nome del corriere.
     */
    String getName();

    /**
     * restituisce il cognome del corriere.
     *
     * @return il cognome del corrire.
     */
    String getCognome();

    ;

}
